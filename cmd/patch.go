package main

import (
	"context"

	"github.com/spf13/cobra"

	"ocibundle/internal/action"
)

var patchOpts action.PatchOpts

// patchCmd applies patch to target the form of OCI-image layout
var patchCmd = &cobra.Command{
	Use:           "patch [PATCH_PATH]",
	Short:         "apply patch file, the targets are written in patch file if patch file is generated by `diff' cmd",
	SilenceUsage:  true,
	SilenceErrors: true,
	Args:          cobra.ExactArgs(1),
	RunE: func(cmd *cobra.Command, args []string) error {
		return action.Patch(context.TODO(), args[0], &patchOpts)
	},
}

func init() {
	rootCmd.AddCommand(patchCmd)
	flag := patchCmd.Flags()
	flag.StringVarP(&patchOpts.Origin, "source", "s", "", "specified a source reference, it will be ignored if patch file is standard (generated by `diff' cmd)")
}
